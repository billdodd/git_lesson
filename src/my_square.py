
def my_square(y):
    """
    takes an integer value and returns the squared value
    uses the ** operator
    """
    return(y ** 2)

def my_square2(x):
    """
    takes an integer value and returns the squared value
    uses the ** operator
    """
    return(x * x)

print(my_square(42))
print(my_square2(42))
